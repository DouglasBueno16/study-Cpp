// Exemplos de if e else

#include <iostream>

using namespace std;

int main() 
{
    int num = 10;

    if (num > 5) {  

        if (num < 15) {
            cout << "O numero esta entre 5 e 15" << endl;
        }
        else {
            cout << "O numero nao esta entre 5 e 15" << endl;
        }
    }    
    return 0;
} // main()
